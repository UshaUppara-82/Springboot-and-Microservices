package com.dto;

import java.time.LocalDate;

public class CustomerDTO {
	
	private long id; //(UUID)
	private String firstName;// (String)
	private String lastName;// (String)
	private String email;// (String)
	private String phone;// (String)
	private LocalDate dateOfBirth;// (Date)
	private int loyaltyPoints;// (Integer)
	
	
	
	
	
	public CustomerDTO(long id, String firstName, String lastName, String email, String phone, LocalDate dateOfBirth,
			int loyaltyPoints) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.phone = phone;
		this.dateOfBirth = dateOfBirth;
		this.loyaltyPoints = loyaltyPoints;
	}
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public LocalDate getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(LocalDate dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public int getLoyaltyPoints() {
		return loyaltyPoints;
	}
	public void setLoyaltyPoints(int loyaltyPoints) {
		this.loyaltyPoints = loyaltyPoints;
	}
	
	

}
